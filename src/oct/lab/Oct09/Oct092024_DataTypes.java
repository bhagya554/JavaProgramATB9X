package oct.lab.Oct09;

public class Oct092024_DataTypes {
    public static void main(String[] args) {
        //Primitive DataType
        byte b1 = 12; //1 Byte = 8 Bits
        short s1 = 129; // 2 Bytes = 16 Bits
        int i1 = 890; //4 Bytes = 32 Bits
        long l1 = 2323424324l;//8 Bytes = 64 Bits

        char c1 = 'r'; // 2 Bytes = 16 Bits

        float f1 = 45.89f; //4 Bytes = 32 Bits
        double d1 = 7987.34324;//8 Bytes = 64 Bits

        boolean bl1 = true; // 1 Byte = 8 Bits

        //Non-Primitive DataType - Any Non-Primitive datatype has size as 8 Bytes.
        String st1 = "Welcome to Jaava";//8 Bytes = 64 Bits

    }
}
