package oct.lab;

public class Oct092024_characters {
    public static void main(String[] args) {
        char c1 = 'r'; // 2 Bytes = 16 Bits
        System.out.println(c1);

        char c2 = '$';
        System.out.println(c2);

        char c3 = '\t';//tab
        System.out.print(c3);

        char c4 = 'r';
        System.out.print(c4);

        char c5 = '\n';
        System.out.print(c5);

        char c6 = '\u1F6A';
        System.out.print(c6);
    }
}
